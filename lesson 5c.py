# tuples uses () to enclose the values

marks = (40, 69, 90, 90, 34)
print(marks)

towns = ("kisumu", "eld", "nakuru", "thika", "nairobi", "machakos")
print(towns)

# we doing slicing
print(marks[0])  # single item
print(marks[0:10])  # print from 0
print(marks[5:10])
print(marks[:10])
print(marks[:6])
print(marks[:])  # prints all the items

print(towns[2:4])  # from nakuru
print(towns[3:])  # thika and the rest

# a tuple is immutable/ cannot be updated
