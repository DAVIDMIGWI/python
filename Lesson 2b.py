# conditional statements they are also known as control statement
# they are use to make decisions

# if if, else, else if.
a = 99
b = 99
if a > b:
        print("Great, A is bigger than B") # This works if condition is true

elif a == b:
    print("A and B  are equal") # another condition


else:
    print("Good, B is bigger") # else is the alternative

# else works if the other conditions are false


